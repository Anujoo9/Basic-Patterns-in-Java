
public class reversehill {

	public static void main(String[] args) {
		int row2=0;
		int star20=5;
		while (row2<=5) {
			int dash20=0;
			while(dash20<=row2) {
				System.out.print("  ");
				dash2++;		}
			int star21=0;
		while(star21<=star2) {
			System.out.print(" *");
			star21+=1;
			
		}
		int star31=0;
		while(star31<=star20-1) {
			System.out.print(" *");
			star31+=1;
			
		}
			row2++;
			star20--;
			System.out.println();
		}
			
	}

}
